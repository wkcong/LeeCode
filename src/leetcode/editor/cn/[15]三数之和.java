        //1.é¦–å…ˆå¯¹æ•°ç»„è¿›è¡Œæ’åº,æ’åºçš„ç›®çš„æ˜¯æ–¹é¢åé¢çš„æŒ‡é’ˆå‘å‰ç§»åŠ¨
        Arrays.sort(nums);
